use std::{fmt::Write, fs, path::Path};

fn collect_mods(dir: &Path, prelude_exports: &mut Vec<String>, mod_path_prefix: &str) {
    let mut mods = vec![];

    for entry in fs::read_dir(dir).expect("Expected read dir to succeed") {
        let path = entry.expect("Failed to get dir entry").path();

        if path.is_dir() {
            let name = path
                .file_name()
                .expect("Failed to get file name")
                .to_string_lossy()
                .into_owned();
            let child_prefix = format!("{mod_path_prefix}::{name}");
            collect_mods(&path, prelude_exports, &child_prefix);
            mods.push(name);
        } else if path.extension().is_some_and(|e| e == "rs")
            && path.file_stem().is_some_and(|e| e != "lib")
        {
            let stem = path
                .file_stem()
                .expect("Failed to get file stem")
                .to_string_lossy();
            if stem != "mod" {
                let types = extract_pub_types(&path);
                for ty in types {
                    prelude_exports.push(format!("pub use {mod_path_prefix}::{stem}::{ty};"));
                }
                mods.push(stem.into_owned());
            }
        }
    }

    if mods.is_empty() {
        return;
    }

    let content = mods.iter().fold(String::new(), |mut acc, s| {
        writeln!(acc, "pub mod {s};").expect("Failed to write");
        acc
    });

    let mod_rs = dir.join("mod.rs");
    if fs::read_to_string(&mod_rs).ok().as_deref() != Some(&content) {
        fs::write(&mod_rs, content).expect("Failed to write to file");
    }

    println!("cargo:rerun-if-changed={}", dir.display());
}

fn extract_pub_types(path: &Path) -> Vec<String> {
    let src = fs::read_to_string(path).expect("Failed to read file");
    let mut types = vec![];

    for line in src.lines() {
        let line = line.trim();
        for keyword in ["pub struct ", "pub enum ", "pub type "] {
            if let Some(rest) = line.strip_prefix(keyword) {
                let name = rest
                    .split(|c: char| !c.is_alphanumeric() && c != '_')
                    .next()
                    .unwrap_or("")
                    .to_string();
                if !name.is_empty() {
                    types.push(name);
                }
                break;
            }
        }
    }

    types
}

fn main() {
    let schema_dir = Path::new("src/schema/");
    let mut prelude_exports = vec![
        "// generated by build.rs DO NOT EDIT".to_string(),
        "#![allow(unused_imports)]".to_string(),
    ];

    collect_mods(schema_dir, &mut prelude_exports, "crate::schema");

    prelude_exports.sort();
    prelude_exports.dedup();

    let prelude = prelude_exports.join("\n");
    let prelude_path = schema_dir.join("prelude.rs");

    if fs::read_to_string(&prelude_path).ok().as_deref() != Some(&prelude) {
        fs::write(&prelude_path, prelude).expect("Failed to write file");
    }
}
